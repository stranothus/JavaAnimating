package Utils;

public class JFrameSize {
    static double widthRatio = 1.0;
    static double heightRatio = 1.0;

    public double getWidth() {
        return widthRatio;
    }
    public void setWidth(double newWidthRatio) {
        widthRatio = newWidthRatio;
    }
    public double getHeight() {
        return heightRatio;
    }
    public void setHeight(double newHeightRatio) {
        heightRatio = newHeightRatio;
    }
}